class Caculator():

    def add(self, number_1, number_2):
        return number_1 + number_2

    def minus(self, number_1, numer_2):
        return number_1 - numer_2

    def multiple(self, number_1, number_2):
        return number_1 * number_2

    def divide(self, number_1, number_2):
        return number_1 / number_2
